package co.com.elis.core.document.validation;

import static java.lang.annotation.ElementType.ANNOTATION_TYPE;
import static java.lang.annotation.ElementType.CONSTRUCTOR;
import static java.lang.annotation.ElementType.PARAMETER;
import static java.lang.annotation.ElementType.TYPE;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;
import javax.validation.Constraint;
import javax.validation.Payload;

@Constraint(validatedBy = {InvoicingRangeInvoiceValidator.class})
@Target({TYPE, ANNOTATION_TYPE, CONSTRUCTOR, PARAMETER})
@Retention(RetentionPolicy.RUNTIME)
public @interface InvoicingRangeValid {

    public String message() default "ELIS_CORE_VAL_DOC_NUMBER_CONSECUTIVE_IN_RANGE";

    public Class<? extends Payload>[] payload() default {};

    public Class<?>[] groups() default {};
}
